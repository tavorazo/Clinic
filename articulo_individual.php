					<p style="text-align:justify">
						<br><br>Es el desalojo o salida total de un diente completo de su cavidad o alveolo. 
						Más de 5 millones de dientes son perdidos cada año, por accidentes, choques o caídas.
						La avulsión dental es la expulsión de un diente integro por una causa traumática como puede ser la caída de una bicicleta, un tropezón, la práctica de algún deporte de contacto (rugby, basket,…) sin la utilización de protector bucal o golpes a la altura de la base de la nariz ocasionados por juegos, accidentes,…
						<br><br>
						La avulsión dental puede ocurrir tanto en dientes temporales como en los dientes permanentes. La diferencia es el tratamiento, ya que los dientes temporales o deciduos no se reimplantan.
						<br><br>
						Los dientes más comúnmente avulsionados en ambas denticiones, la temporal y la permanente, son los incisivos centrales maxilares; y el grupo de edad más afectado generalmente es entre 7 y 11 años.
						<br><br>
						El sexo masculino presenta una incidencia de avulsiones tres veces mayor que el sexo femenino.
						<br><br>
						Cuando el diente es avulsionado, se produce un trauma al periodonto generando una inflamación localizada en el ligamento periodontal.
						<br><br>
						<strong>¿Cómo debemos actual ante uan avulisión dental?</strong>
						<br><br>
						Primeramente debemos actuar con tranquilidad y rapidez, de esto depende el éxito del tratamiento ya que un dente reubicado en un espacio de tiempo corto, de 30 a 120 minutos, tiene mayores posibilidades de reinsertarse con éxito.
						<br><br>
						Actitud en el lugar de la avulsión dental:

						<li>Buscar el diente y cogerlo por la parte de la corona, evitar siempre tocar la raíz.</li>
						<li>Si el diente está sucio, limpiarlo con agua del grifo con cuidado de no tocar la raíz del diente, sin utilizar jabones o substancias desinfectantes.</li>
						<li>Asegurarse que el diente avulsionado es un diente permanente ya que los dientes temporales no deben ser reimplantados.</li>
						<li>Si es posible, reimplantarlo inmediatamente.</li>
						<li>Si no podemos reimplantarlo de inmediato, colocar el diente en el mejor medio de transporte posible.</li>
						<li>Acudir de inmediato a un centro donde puedan recibir tratamiento dental de urgencia.</li>
